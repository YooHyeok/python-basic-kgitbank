
"""
* 실수형 (floation point)

-실수형 자료에는 10진수형 소수점 표현방식과
지수형 표현방식을 사용한 실수값을 저장할 수 있습니다.
"""
a = 85.421
print(type(a))

b = 100.0
print(type(b))

c = 9.832e13
print(c)

d = 3.14159e-3
print(d)

'''
* 복소수형 (complex)

- 복소수는 제곱해서 음수가 되는 가상의 숫자입니다.
- 허수를 나타내는 접미사는 j를 사용합니다.
'''
e = 1 + 2j
print(type(e))





















