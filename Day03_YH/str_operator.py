
'''
* 문자열 연산

- 파이썬은 문자열의 덧셈연산과 곱센연산을 지원합니다.
- 문자열의 덧셈 연산시 문자열을 서로 연결하여 결합합니다.
'''
s1 = "오늘 저녁은 "
s2 = "치킨입니다."
print(s1 + s2 + "맛있겠다~~~")
print("오늘 저녁은 " + "치킨입니다.")

'''
- 파이썬에서는 문자열의 곱셈연산 또한 지원하고 있습니다.
- 곱셈연산자(*)로 문자열을 정해진 수 만큼 반복 연결할 수 있십니다.

- 문자열과 문자열끼리 곱하거나 문자열과 실수를 곱할 수는 없습니다.
'''
print("_" * 40)
print(40 * "_")

print("배고파" * 4)

